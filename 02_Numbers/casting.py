# it is done using constructor function
# to specify type on to a variable

# int() - constructs an integer number
num1 = int(12) # 12
num2 = int(1.6) # 2
num3 = int('12') # 12
print(num3, type(num3))

# float() - constructs a float number
float1 = float(1) #1.0
float2 = float("4") # 4.0
float3 = float("4.6") #4.6
print(float3, type(float3))


# str() - constructs a string from a wide variety of data types
str1 = str(23) #'23'
str2 = str(4.65) #'4.65'
str3 = str("string") #'string'
print(str3, type(str3))
